
public class Goldi {

	int g_weight;
	int g_maxTemp;

	public Goldi() {
	}

	public Goldi(int weight, int maxTemp) {
		this.g_weight = weight;
		this.g_maxTemp = maxTemp;
	}

	// Getters and Setters
	public int getG_weight() {
		return g_weight;
	}

	public int getG_maxTemp() {
		return g_maxTemp;
	}

}
